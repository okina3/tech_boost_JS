//課題【フロントエンド】09 コール関数。の課題
//1~100までの配列----------------------------------------------
a = [];
for (i = 1; i <= 100; i++) {
   a.push(i);
}
console.log(a);

//3の倍数のみが入っている配列-------------------------------------------
let b3 = a.filter(e => e % 3 == 0);
console.log(b3);

//5の倍数のみが入っている配列-------------------------------------------
let b5 = a.filter(e => e % 5 == 0);
console.log(b5);

